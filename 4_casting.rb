# type casting is done using to_[data-type] syntax

puts 1.to_f # int to float
puts 1.to_s # '' to string
print (2..5).to_a # '' array
puts ""
puts 3.14.to_i # float to int
puts 3.14.to_s # '' to string
 
puts "123".to_i # string to in
puts "123".to_f # '' float
puts "345".to_r # '' rational
puts "474".to_c # '' complex 

